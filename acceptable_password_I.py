"""
Вы находитесь в начале серии паролей. Каждая миссия основана на предыдущей. Следующие миссии станут немного сложнее.

В этой миссии вам нужно создать функцию проверки пароля.

Условие проверки:

длина должна быть больше 6.
Вход: строка.

Выход: Бул.

Пример:

is_acceptable_password ( 'короткий' ) == Ложь
is_acceptable_password ( 'гораздо дольше' ) == Истина
"""


def is_acceptable_password(password: str) -> bool:
    if len(password) <= 6:
        return False
    else:
        return True
